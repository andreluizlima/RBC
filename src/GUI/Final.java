    public Final() {
        initComponents();
        DataAccess dta = new DataAccess();
        dta.setBase("data/base.csv");
        data = dta.getBaseCompleta();
        String s[] = new String[37];
        for (int i = 0; i < data.getBase().get(0).getAtbValues().size(); i++) {
            s[i] = data.getBase().get(0).getAtbValues().get(i).getAtributo();
        }
        DefaultTableModel d = new DefaultTableModel(s, 0);
        data.reSort();
        for (int i = 0; i < data.getBase().size(); i++) {

            Object[] o = new Object[37];
            for (int j = 0; j < data.getBase().get(i).getAtbValues().size(); j++) {
                o[j] = data.getBase().get(i).getAtbValues().get(j).getValor();
            }
            d.addRow(o);
        }
        tabelao.setModel(d);
        Base.id = 0;
    }

